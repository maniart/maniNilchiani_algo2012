#include "dust.h"
dust::dust(){
	damping= .2;
}
//------------------------------------------------------------------------------------------------
void dust::setInitialConditions(float px, float py, float vx, float vy){
	vel.set(vx,vy);
	pos.set(px,py);
}
//------------------------------------------------------------------------------------------------
void dust::addForce(float x, float y){
	frc.x += x;
	frc.y += y;
}
//------------------------------------------------------------------------------------------------
void dust::resetForce(){
	frc.set(0,0);
}
//------------------------------------------------------------------------------------------------
void dust::addDampingForce(){
	frc.x -= vel.x*damping;
	frc.y -= vel.y*damping;
}
//------------------------------------------------------------------------------------------------
void dust::update(){
	vel += frc;
	pos += vel;
}
//------------------------------------------------------------------------------------------------
void dust::draw(){
	ofCircle(pos.x, pos.y, 2);
	ofNoFill();
	
}
